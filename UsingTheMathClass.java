//  Topic: Math class:
// For u to call a static method, you willl need to create a class name 

public class UsingTheMathClass{
	public static void main (String [] args){
		System.out.printf("The absolute is %d%n",Math.abs(-90));
		System.out.printf("The highest value is %d%n",Math.max(80,90));
		System.out.printf("The lowest value is %d%n",Math.min(69,2));
		System.out.printf("8 raise to the power of 2 is %f%n",Math.pow(8,2));
		System.out.printf("The generated number is %f%n",Math.random());
	}
}